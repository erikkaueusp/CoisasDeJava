
public class Cliente {
	private String nome;
	private int idade;
	private String sexo;
	
	
	public String getNome() {
		return this.nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public int getIdade() {
		return idade;
	}
	
	public boolean setIdade(int idade) {
		if (idade >= 18) {
			this.idade = idade;
			return true;
		}
		else {
			return false;
		}
	}
	public String getSexo() {
		return sexo;
	}
	public boolean setSexo(String sexo) {
		if (sexo == "masculino" || sexo == "feminino") {
			this.sexo = sexo;
			return true;
		}
		else {
			return false;
		}
	}

}
